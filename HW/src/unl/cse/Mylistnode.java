package unl.cse;

public class Mylistnode {
	
	private Mylistnode next;
    private Portfolio item;
    private Mylistnode prev;
    

    public Mylistnode(Portfolio item) {
        this.item = item;
        this.next = null;
        this.prev = null;
    }

    public Portfolio getItem() {
		return item;
	}

	public void setItem(Portfolio item) {
		this.item = item;
	}

	public Mylistnode getPrev() {
		return prev;
	}

	public void setPrev(Mylistnode prev) {
		this.prev = prev;
	}

	public void setNext(Mylistnode next) {
		this.next = next;
	}
	
	public Mylistnode getNext(){
		return next;
	}

}
