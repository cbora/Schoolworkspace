package unl.cse.weiss;

public class Problem5_20 {

	public static void main(String args[]) {
		
		int n = 10;
		long sum = 0;
		
		// Fragment 1
		for(int i=0; i<n; i++) 
			sum++;
		System.out.println("Sum = "+sum);
		sum = 0;
		
		// Fragment 2
		for(int i=0; i<n; i+=2) 
			sum++;
		System.out.println("Sum = "+sum);
		sum = 0;
		
		// Fragment 3 
		for(int i=0; i<n; i++) 
			for(int j=0; j<n; j++) 
				sum++;
		System.out.println("Sum = "+sum);
		sum = 0;
		
		// Fragment 4
		for(int i=0; i<n; i++) 
			sum++;
		for(int j=0; j<n; j++) 
			sum++;
		System.out.println("Sum = "+sum);
		sum = 0;
		
		// Fragment 5
		for(int i=0; i<n; i++) 
			for(int j=0; j<n*n; j++) 
				sum++;
		System.out.println("Sum = "+sum);
		sum = 0;
		
		// Fragment 6
		for(int i=0; i<n; i++) 
			for(int j=0; j<i; j++) 
				sum++;
		System.out.println("Sum = "+sum);
		sum = 0;

		// Fragment 7
		for(int i=0; i<n; i++) 
			for(int j=0; j<n*n; j++) 
				for(int k=0; k<j; k++) 
					sum++;
		System.out.println("Sum = "+sum);
		sum = 0;

		// Fragment 8
		for(int i=1; i<n; i=i*2) 
			sum++;
		System.out.println("Sum = "+sum);
		sum = 0;

	}
}
